package com.example.travelhub.hotelbooking.models;

public class Instruction {
    
    private String type;
    private String msg;
    
    public Instruction() {
    }
    
    public String getType() {
        return type;
    }
    
    public void setType(String type) {
        this.type = type;
    }
    
    public String getMsg() {
        return msg;
    }
    
    public void setMsg(String msg) {
        this.msg = msg;
    }
}