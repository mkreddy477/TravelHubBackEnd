package com.example.travelhub.hotelbooking.models;

public class BedInfo {
    
    private String bt;
    private String bc;
    
    public BedInfo() {
    }
    
    public String getBt() {
        return bt;
    }
    
    public void setBt(String bt) {
        this.bt = bt;
    }
    
    public String getBc() {
        return bc;
    }
    
    public void setBc(String bc) {
        this.bc = bc;
    }
}